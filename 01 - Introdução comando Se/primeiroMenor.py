"""
Introdução - ler dois inteiros e imprimir 1 se primeiro menor
Construa um algoritmo que recebe dois números inteiros como entradas (digamos na variáveis a e b) e imprima 1 se ocorrer "a<b" e 0 
em caso contrário.
"""
